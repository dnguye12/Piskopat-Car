function sampleArray<T>(arr: T[], n: number): T[] {
    const a = arr.slice();
    const count = Math.min(n, a.length);
    for (let i = a.length - 1; i > a.length - 1 - count; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [a[i], a[j]] = [a[j], a[i]];
    }
    return a.slice(a.length - count);
}

